fun main() {
    // if expression
    val nilai = 90

    if (nilai > 75){
        println("Good job")
    }

    // else expression
    val nilaii = 70

    if (nilaii > 75){
        println("Good job")
    }else{
        println("jangan menyerah")
    }

    // else if expression
    val skor = 55

    if (skor > 90){
        println("sempurna")
    }else if (skor > 75){
        println("masih aman")
    }else if (skor > 50){
        println("lebih kerj keras lagi ya...")
    }else{
        println("jangan menyerah")
    }
}